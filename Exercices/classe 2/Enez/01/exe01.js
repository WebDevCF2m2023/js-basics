
function writeNumbers() {
    for (let i = 1; i <= 10; i++) {
        console.log(i);
    }
}

writeNumbers();


function writeEvenNumbers() {
    for (let i = 2; i <= 10; i += 2) {
        console.log(i);
    }
}

writeEvenNumbers();


function writeNumbersReverse() {
    for (let i = 10; i >= 1; i--) {
        console.log(i);
    }
}

writeNumbersReverse();

